function bukaTutupMenu() {
    var x = document.getElementById("wmodal");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function tutupmenu() {
    var x = document.getElementById("wmodal");
    x.style.display = "none";
}